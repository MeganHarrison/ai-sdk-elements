# Script to update Supabase types locally
# Usage: ./scripts/update-types.sh

set -e

echo "ğŸ”„ Updating Supabase database types..."
echo "â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”"

# Check if SUPABASE_ACCESS_TOKEN is set
if [ -z "$SUPABASE_ACCESS_TOKEN" ]; then
    echo "âš ï¸  Warning: SUPABASE_ACCESS_TOKEN not set"
    echo "   You may need to login with: npx supabase login"
fi

# Project configuration
PROJECT_ID="lgveqfnpkxvzbnnwuled"
OUTPUT_FILE="database.types.ts"

# Generate types
echo "ğŸ“¡ Fetching schema from Supabase project: $PROJECT_ID"
npx supabase gen types typescript \
    --project-id $PROJECT_ID \
    --schema public \
    > $OUTPUT_FILE.tmp

# Check if generation was successful
if [ $? -eq 0 ]; then
    # Add header comment to the file
    cat > $OUTPUT_FILE << EOF
/**
 * Supabase Database Types
 * 
 * Auto-generated from Supabase project: $PROJECT_ID
 * Generated at: $(date -u '+%Y-%m-%d %H:%M:%S UTC')
 * 
 * DO NOT EDIT THIS FILE DIRECTLY
 * Run 'npm run update-types' to regenerate
 */

EOF
    
    # Append the generated types
    cat $OUTPUT_FILE.tmp >> $OUTPUT_FILE
    rm $OUTPUT_FILE.tmp
    
    echo "âœ… Types updated successfully!"
    echo ""
    echo "ğŸ“„ Output file: $OUTPUT_FILE"
    
    # Check for changes
    if git diff --quiet $OUTPUT_FILE 2>/dev/null; then
        echo "â„¹ï¸  No changes detected in database types"
    else
        echo "âœ¨ Database types have been updated"
        echo ""
        echo "Changes:"
        git diff --stat $OUTPUT_FILE 2>/dev/null || true
    fi
else
    echo "âŒ Failed to generate types"
    rm -f $OUTPUT_FILE.tmp
    exit 1
fi

echo ""
